public class cliente {
    private int numero_afilador;
    private String nombre;
    private int edad;
    private String  direccion;
    private String rfc;

    public int getNumero_afilador() {
        return numero_afilador;
    }

    public void setNumero_afilador(int numero_afilador) {
        this.numero_afilador = numero_afilador;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getRfc() {
        return rfc;
    }

    public void setRfc(String rfc) {
        this.rfc = rfc;
    }
}
